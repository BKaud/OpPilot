    var timerFills   = document.querySelectorAll('[id^="timerFill"]');

    function formatTime(sec) {
        var m = Math.floor(sec / 60);
        var s = sec % 60;
        return (m < 10 ? '0' : '') + m + ':' + (s < 10 ? '0' : '') + s;
    }

    function updateTimerUI(remaining) {
        var pct = (remaining / ROT_DURATION) * 100;
        timerLabels.forEach(function(el) { el.textContent = formatTime(remaining); });
        timerFills.forEach(function(el) {
            el.style.width = pct + '%';
            el.style.background = pct > 40 ? 'var(--lp-teal)' : pct > 15 ? '#e6a817' : '#c0392b';
        });
    }

    if (rotBtn) {
        rotBtn.addEventListener('click', function() {
            if (rotInterval) {
                clearInterval(rotInterval);
                rotInterval = null;
                rotBtn.textContent = '▶ Start Rotation';
