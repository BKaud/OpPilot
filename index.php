                updateTimerUI(rotRemaining);
            } else {
                rotBtn.textContent = '⏹ Stop';
                rotInterval = setInterval(function() {
                    // Speed up simulation: each tick = 30 real seconds
                    rotRemaining = Math.max(0, rotRemaining - 30);
                    updateTimerUI(rotRemaining);
                    if (rotRemaining === 0) {
                        clearInterval(rotInterval);
                        rotInterval = null;
                        rotBtn.textContent = 'Rotation Complete — Restart';
                        document.querySelectorAll('.lp-zone-card').forEach(function(c) {
                            c.classList.add('lp-zone-card--pulse');
                            setTimeout(function() { c.classList.remove('lp-zone-card--pulse'); }, 1200);
