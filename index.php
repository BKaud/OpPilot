(function () {
    // ── Hero text reveal ──────────────────────────────────────
    ['heroLine1','heroLine2'].forEach(function(id, i) {
        var el = document.getElementById(id);
        if (!el) return;
        el.style.opacity = '0';
        el.style.transform = 'translateY(20px)';
        el.style.transition = 'opacity 0.7s ease, transform 0.7s ease';
        setTimeout(function() {
            el.style.opacity = '1';
            el.style.transform = 'translateY(0)';
        }, 200 + i * 180);
    });

    // ── Counter animation ─────────────────────────────────────
    function animateCounter(el) {
        var target = +el.dataset.target;
        var dur = 1800;
        var start = null;
        function step(ts) {
            if (!start) start = ts;
            var progress = Math.min((ts - start) / dur, 1);
            var ease = 1 - Math.pow(1 - progress, 3);
            el.textContent = Math.round(ease * target) + (target === 99 ? '%' : '+');
            if (progress < 1) requestAnimationFrame(step);
        }
        requestAnimationFrame(step);
    }

    var counters = document.querySelectorAll('.lp-stat-num');
    var countersStarted = false;
    var observer = new IntersectionObserver(function(entries) {
        if (countersStarted) return;
        entries.forEach(function(e) {
            if (e.isIntersecting) {
                countersStarted = true;
                counters.forEach(animateCounter);
            }
        });
    }, { threshold: 0.4 });
    if (counters[0]) observer.observe(counters[0]);

    // ── Tab switcher ──────────────────────────────────────────
    document.querySelectorAll('.lp-demo-tab').forEach(function(tab) {
