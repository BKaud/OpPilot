    var statuses   = ['open', 'standby', 'down'];
    var rideInterval = null;
    var rideBtn   = document.getElementById('rideToggleBtn');
    var rideCards = document.querySelectorAll('.lp-ride-card');

    function randomRideUpdate() {
        var card = rideCards[Math.floor(Math.random() * rideCards.length)];
        var current = card.dataset.status;
        var candidates = statuses.filter(function(s) { return s !== current; });
        var next = candidates[Math.floor(Math.random() * candidates.length)];
        card.classList.add('lp-ride-card--flash');
        setTimeout(function() {
            card.dataset.status = next;
            var chip = card.querySelector('.lp-pos-chip');
            chip.textContent = next.charAt(0).toUpperCase() + next.slice(1);
            chip.className = 'lp-pos-chip lp-chip-' + next;
            var dot = card.querySelector('.lp-ride-sdot');
            dot.className = 'lp-ride-sdot' + (next === 'open' ? '' : next === 'standby' ? ' warn' : ' off');
            card.classList.remove('lp-ride-card--flash');
        }, 300);
    }

    if (rideBtn) {
